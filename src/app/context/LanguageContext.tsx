// For languages without full translations, default to English.
const TRANSLATION_MAP: Partial<Record<LangCode, Translations>> = {
  EN, HI, BN, TE, MR, TA, PA, KN, GU, OR, ML, AS, UR,
  SA, KOK, SD, KS, DOG, MAI, NE, BOD, MNI, SAT,
};

export function getTranslations(code: LangCode): Translations {
  return TRANSLATION_MAP[code] ?? EN;
}

// ─── Context ──────────────────────────────────────────────────────────────────
interface LanguageContextValue {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: Translations;
}

const LanguageContext = createContext<LanguageContextValue>({
  language: LANGUAGES[0],
  setLanguage: () => {},
  t: EN,
});

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem('schemme_lang');
    if (saved) {
      const found = LANGUAGES.find(l => l.code === saved);
      if (found) return found;
    }
    return LANGUAGES[0];
  });
  
  const [t, setT] = useState<Translations>(() => {
    return getTranslations(language.code);
  });

  const setLanguage = (lang: Language) => {
    localStorage.setItem('schemme_lang', lang.code);
    setLanguageState(lang);
    setT(getTranslations(lang.code));
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => useContext(LanguageContext);
